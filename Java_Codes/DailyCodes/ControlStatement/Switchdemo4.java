class Switchdemo4 {
    public static void main(String[] args) {
        char ch = 'E';
        switch(ch) {
            case 'A':
                System.out.println("A");
                break;
            case 'B':
                System.out.println("B");
                break;
            case 'C':
                System.out.println("C");
                break;
            case 'D':
                System.out.println("D");
                break;
            default:
                System.out.println("Wrong Input");
        }
    }
}

