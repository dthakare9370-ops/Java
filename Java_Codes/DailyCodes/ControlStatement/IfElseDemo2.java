class IfElseDemo2{
       public static void main(String args[]){
	       int x=7;
	       int y=8;
	       int z=10;

	       if(x>y && x>z){
		       System.out.println(x + " is Greater");
	       }else if(y>z){
		       System.out.println(y + " is Greater");
	       }else{
		       System.out.println(z + " is Greater");
	       }
       }
}

