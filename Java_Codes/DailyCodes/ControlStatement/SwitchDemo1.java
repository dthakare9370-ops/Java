class SwitchDemo1{
	public static void main(String args[]){
		String ch = "ello";

		switch(ch){
			default:
				System.out.println("Invalide Input");
				break;
			case "one": System.out.println("one");
				break;
			case "two": System.out.println("Two");
				break;
			case "hello":	System.out.println("Hello Ganu");
					break;
			case "A" :
					System.out.println("A");
					break; 
		}
	}
}
