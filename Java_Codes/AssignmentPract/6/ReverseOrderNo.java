import java.util.*;
class ReverseOrderNo{
        public static void main(String args[]){
                Scanner sc = new Scanner(System.in);
                System.out.println("Enter the Number :- ");
                int no = sc.nextInt();

                for(int i=no;i>0;i--){
                        System.out.print(i+" ");
                }
                System.out.println();                                                       }                                                                           } 
